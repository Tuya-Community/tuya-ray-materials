export type DefaultSceneItem = ReturnType<typeof getScenes>[0];

/**
 * 在 defaultScenes 中查找匹配的场景
 * @param key - 场景的唯一标识符
 * @returns 包含场景类别和对象的 { category: SceneCategory; scene: any } 类型的值，若未找到则返回 undefined
 */
export function findScene(
  defaultScenes: any,
  key: number
): { category: SceneCategory; scene: any } | undefined {
  for (const category in defaultScenes) {
    if (Object.prototype.hasOwnProperty.call(defaultScenes, category)) {
      const scene = defaultScenes[category].find(item => item.key === key);
      if (scene) {
        return { category: SceneCategory[category as keyof typeof SceneCategory], scene };
      }
    }
  }
  return undefined;
}
